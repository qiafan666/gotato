func cmpInt8s(a, b []int8) bool {
	if a == nil && b == nil {
		return true
	}
	if a == nil || b == nil {
		return false
	}
	if len(a) != len(b) {
		return false
	}
	for i := range a {
		if a[i] != b[i] {
			return false
		}
	}
	return true
}

func checkNAF(t *testing.T, testValStr string, resSlice []int8) {
	testVal := &big.Int{}
	testVal, _ = testVal.SetString(testValStr, 10)
	res := NAF(testVal, 2)
	if !cmpInt8s(res, resSlice) {
		t.Errorf("Invalid / incompatible result for NAF: expected %v, got %v", resSlice, res)
	}
	if strings.Compare(testVal.String(), testValStr) != 0 {
		t.Errorf("Input value should not be altered/stepped-on by NAF function")
	}
}
